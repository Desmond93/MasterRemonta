$('.prices_top_1').on('click', function() {
  if ($('.prices_top_1 .plus').text() == '+') { 
    $('.table_1').css('display', 'table');
    $('.table_1 tbody').css('display', 'table-row-group');    
    $('.prices_top_1 .plus').html('-');
    $('.prices_top_1').css('background-color', '#8fa79e');
    $('.prices_top_1 .plus').css('color', '#1e453e'); 
    $('.prices_top_1 h4').css('color', '#1e453e');       
  }
  else {
    $('.table_1').css('display', 'none');
    $('.table_1 tbody').css('display', 'none');
    $('.prices_top_1 .plus').html('+');
    $('.prices_top_1').css('background-color', '#1e453e');
    $('.prices_top_1 .plus').css('color', '#d9e3dd'); 
    $('.prices_top_1 h4').css('color', '#d9e3dd'); 
}  
});

